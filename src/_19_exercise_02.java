public class _19_exercise_02 {

    public static void main(String[] args) {
        
        // print the following star pattern
        // ****
        // ***
        // **
        // *
        

        for (int i = 1; i <= 4; i++) {
            for (int j = 4; j >= i; j--) {
                System.out.print("*");
            }
            System.out.println();
        }
        


        
        
    }
    
}
